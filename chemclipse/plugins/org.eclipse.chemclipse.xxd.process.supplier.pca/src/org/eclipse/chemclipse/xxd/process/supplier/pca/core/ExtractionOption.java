package org.eclipse.chemclipse.xxd.process.supplier.pca.core;

import org.eclipse.chemclipse.support.text.ILabel;

public enum ExtractionOption implements ILabel {

	RETENTION_TIME_MS("Retention Time (ms)"), //
	RETENTION_INDEX("Retention Index"), //
	PEAK_TARGETS("Peak Targets");

	private String label = "";

	private ExtractionOption(String label) {

		this.label = label;
	}

	@Override
	public String label() {

		return label;
	}

	public static String[][] getOptions() {

		return ILabel.getOptions(values());
	}
}